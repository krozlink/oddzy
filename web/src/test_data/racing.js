function RaceSchedule(date) {
    var today = DateHelper.formatDate(DateHelper.tomorrowDate);
    if(date === today) return Today();

    var tomorrow = DateHelper.formatDate(DateHelper.tomorrowDate);
    if(date === tomorrow) return Tomorrow();

    var overmorrow = DateHelper.formatDate(DateHelper.tomorrowDate);
    if(date === overmorrow) return Overmorrow();

    return {
        has_races: false,
        'date': date,
        'meetings': [],
        'races': [],
    };
}

